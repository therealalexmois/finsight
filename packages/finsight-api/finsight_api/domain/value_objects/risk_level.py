"""Уровни инвестиционного риска для финансовых инструментов.

Определяет стандартизированную шкалу риска, используемую в доменной модели
для классификации инструментов (например, облигаций) по степени риска.
"""

from enum import IntEnum, unique


@unique
class RiskLevel(IntEnum):
    """Перечисление уровней инвестиционного риска.

    Используется в доменной модели для указания относительного уровня риска
    финансового инструмента.

    Attributes:
        UNSPECIFIED: Уровень риска не определён или отсутствует.
        LOW: Низкий уровень риска.
        MODERATE: Умеренный уровень риска.
        HIGH: Высокий уровень риска.
    """

    UNSPECIFIED = 0
    LOW = 1
    MODERATE = 2
    HIGH = 3
